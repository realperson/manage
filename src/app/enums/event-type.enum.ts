/**
 * 事件类型
 */
export enum EventType {
    /**
     * 隐藏加载动画
     */
    TYPE_LOADING_HIDE,

    /**
     * 显示加载动画
     */
    TYPE_LOADING,

      /**
       * 隐藏提示
       */
    TYPE_TOAST_HIDE,

      /**
       * 显示提示
       */
    TYPE_TOAST,

      /**
       * 隐藏弹出框
       */
    TYPE_ALERT_HIDE,

      /**
       * 显示弹出框
       */
    TYPE_ALERT
}
